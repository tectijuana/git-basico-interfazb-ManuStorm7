#include <iostream>
using namespace std;

/*CAPITULO 1 PROBLEMAS INTRODUCCTORIOS
EJERCICIO 1

1.-Imprimir los numeros enteros del 9 al 43

POR MANUEL APARICIO ERNESTO GERARDO*/

int main()
{
    // Hacemos un ciclo desde 9 hasta 43
    // Vamos aumentando a i de 1 en 1 con i++
    for (int i = 9; i <= 43; i ++)
    {
        // Imprimimos en pantalla y damos un salto entre cada nunuero
        cout << i << endl;
    }
}